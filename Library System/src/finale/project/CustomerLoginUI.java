    // Method to load customer information from a file
    private void loadInfoFromFile(String fileName) {
        try (BufferedReader reader = new BufferedReader(new FileReader(fileName))) {
            String line;
            while ((line = reader.readLine()) != null) {
                line = line.trim();
                if (!line.isEmpty()) {
                    try {
                        // Read the reference number from the current line
                        int refNum = Integer.parseInt(line);
                        // Read the title from the next line
                        if ((line = reader.readLine()) != null) {
                            String title = line.trim();
                            if (!title.isEmpty()) {
                                customers.add(new Customer(refNum, title));
                            } else {
                                System.err.println("Empty title encountered. Skipping entry.");
                            }
                        } else {
                            System.err.println("No title found for reference number " + refNum + ". Skipping entry.");
                        }
                    } catch (NumberFormatException e) {
                        System.err.println("Invalid reference number format: " + line);
                    }
                } else {
                    System.err.println("Empty line encountered. Skipping entry.");
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

