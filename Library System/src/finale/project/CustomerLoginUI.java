    // Method to perform linear search for a customer by reference number
    private boolean linearSearch(int refNum) {

        continueButton.setVisible(true);
        for (Customer Customer : customers) {
            if (Customer.getReferenceNumber() == refNum) {
                loginFoundOrNotFoundLabel.setText("Welcome back, " + Customer.getTitle());
                return true;
            }

        }
        loginFoundOrNotFoundLabel.setText("User not found.");
        continueButton.setVisible(false);
        return false;
    }

